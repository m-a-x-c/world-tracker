function getPopColor(iso2) {
  const val = COUNTRY_POPULATION[iso2];
  if (!val) return '#555566';
  for (const bin of POP_BINS) {
    if (val <= bin.max) return bin.color;
  }
  return POP_BINS[POP_BINS.length - 1].color;
}
